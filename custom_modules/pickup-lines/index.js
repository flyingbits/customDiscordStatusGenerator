let up;  // updatePresence function
let speed;  // how often the presence updates
let index;  // index of pickup line
let lines;  // list of pickup lines
let interval;  // carries the setInterval function that loops presence updates

function constructor(updatePresenceFunction, loop_speed) {
    up = updatePresenceFunction;
    speed = loop_speed;
    index = 0;
    lines = load_lines();
}

function start() {
    update();
    interval = setInterval(update, speed);
}

function update() {
    let line = lines[index];
    let description = "";
    let status = ";)";
    if (line.length > 45) {
        description = line.substring(0, 45);
        status = line.substring(45);
    } else {
        description = line;
    }
    up(status, description, Date.now());
    index++;
    if (index >= lines.length) {
        index = 0;
    }
}

function stop() {
    clearInterval(interval);
}

module.exports = {constructor, start, stop};